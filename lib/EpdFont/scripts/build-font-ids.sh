#!/bin/bash

set -e

cd "$(dirname "$0")/../builtinFonts"

echo "// The contents of this file are generated by ./lib/EpdFont/scripts/build-font-ids.sh"
echo "#pragma once"
echo ""

# Bookerly reader fonts
for size in 12 14 16 18; do
echo "#define BOOKERLY_${size}_FONT_ID ($(
ruby -rdigest -e "puts [
  \"./bookerly_${size}_regular.h\",
  \"./bookerly_${size}_bold.h\",
  \"./bookerly_${size}_bolditalic.h\",
  \"./bookerly_${size}_italic.h\",
].map{|f| Digest::SHA256.hexdigest(File.read(f)).to_i(16) }.sum % (2 ** 32) - (2 ** 31)"
))"
done

# NotoSans reader fonts
for size in 12 14 16 18; do
echo "#define NOTOSANS_${size}_FONT_ID ($(
ruby -rdigest -e "puts [
  \"./notosans_${size}_regular.h\",
  \"./notosans_${size}_bold.h\",
  \"./notosans_${size}_bolditalic.h\",
  \"./notosans_${size}_italic.h\",
].map{|f| Digest::SHA256.hexdigest(File.read(f)).to_i(16) }.sum % (2 ** 32) - (2 ** 31)"
))"
done

# Lexend reader fonts (no italic)
for size in 12 14 16 18; do
echo "#define LEXEND_${size}_FONT_ID ($(
ruby -rdigest -e "puts [
  \"./lexend_${size}_regular.h\",
  \"./lexend_${size}_bold.h\",
].map{|f| Digest::SHA256.hexdigest(File.read(f)).to_i(16) }.sum % (2 ** 32) - (2 ** 31)"
))"
done

# Bokerlam reader fonts (no bolditalic)
for size in 12 14 16 18; do
echo "#define BOKERLAM_${size}_FONT_ID ($(
ruby -rdigest -e "puts [
  \"./bokerlam_${size}_regular.h\",
  \"./bokerlam_${size}_bold.h\",
  \"./bokerlam_${size}_italic.h\",
].map{|f| Digest::SHA256.hexdigest(File.read(f)).to_i(16) }.sum % (2 ** 32) - (2 ** 31)"
))"
done

# UI fonts (NotoSans 12)
echo "#define UI_10_FONT_ID ($(
ruby -rdigest -e 'puts [
  "./notosans_12_regular.h",
  "./notosans_12_bold.h",
].map{|f| Digest::SHA256.hexdigest(File.read(f)).to_i(16) }.sum % (2 ** 32) - (2 ** 31)'
))"

echo "#define UI_12_FONT_ID ($(
ruby -rdigest -e 'puts [
  "./notosans_12_regular.h",
  "./notosans_12_bold.h",
].map{|f| Digest::SHA256.hexdigest(File.read(f)).to_i(16) }.sum % (2 ** 32) - (2 ** 31)'
))"

echo "#define SMALL_FONT_ID ($(
ruby -rdigest -e 'puts [
  "./notosans_8_regular.h",
].map{|f| Digest::SHA256.hexdigest(File.read(f)).to_i(16) }.sum % (2 ** 32) - (2 ** 31)'
))"
