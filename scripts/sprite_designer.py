#!/usr/bin/env python3
"""Convert ASCII art sprites to C++ uint32_t arrays for PetSpriteData.h.
Each sprite is 24x24. '#' = pixel on, '.' or ' ' = pixel off.
Bit 23 = leftmost column (col 0), bit 0 = rightmost (col 23).

Design principles:
- OUTLINE style: thin 1-2px outline, white interior with minimal features
- Each animal has ONE exaggerated KEY feature for instant recognition
- All sprites face RIGHT
- Silhouettes are maximally distinct from each other
"""

def ascii_to_hex(lines):
    """Convert 24 lines of 24-char ASCII art to list of hex strings."""
    result = []
    for line in lines:
        # Pad or truncate to 24 chars
        row = (line + '.' * 24)[:24]
        val = 0
        for col in range(24):
            if row[col] == '#':
                val |= (1 << (23 - col))
        result.append(f"0x{val:06X}")
    # Pad to 24 rows if needed
    while len(result) < 24:
        result.append("0x000000")
    return result

def format_frame(hexvals, comment=""):
    """Format a single 24-row frame as C++ array initializer."""
    lines = []
    if comment:
        lines.append(f"    {{ // {comment}")
    else:
        lines.append("    {")
    for i in range(0, 24, 6):
        chunk = ",".join(hexvals[i:i+6])
        lines.append(f"      {chunk},")
    lines.append("    },")
    return "\n".join(lines)

def design_sprite(art_string):
    """Parse multiline ASCII art string into hex values.
    Strips leading/trailing blank lines, validates exactly 24 rows of 24 chars."""
    lines = [l for l in art_string.split('\n') if l.strip() or l == '']
    # Remove leading/trailing empty lines
    while lines and not lines[0].strip():
        lines.pop(0)
    while lines and not lines[-1].strip():
        lines.pop()
    assert len(lines) == 24, f"Expected 24 lines, got {len(lines)}"
    for i, line in enumerate(lines):
        # Strip trailing whitespace only, keep leading dots
        stripped = line.rstrip()
        assert len(stripped) == 24, f"Line {i} has {len(stripped)} chars: '{stripped}'"
    return ascii_to_hex([l.rstrip() for l in lines])


