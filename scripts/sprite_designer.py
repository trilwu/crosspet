def output_type(name, stages):
    """Output a complete pet type array."""
    print(f"static const uint32_t k{name}[5][3][24] = {{")
    stage_names = ["EGG", "HATCHLING", "YOUNGSTER", "COMPANION", "ELDER"]
    anim_comments = [
        ["center", "tilt left", "tilt right"],
        ["normal", "blink", "peck/head tilt"],
        ["stand", "step left", "step right"],
        ["normal", "inhale up", "exhale down"],
        ["center", "lean left", "lean right"],
    ]
    for si, (stage, frames) in enumerate(zip(stage_names, stages)):
        print(f"  // [{si}] {stage}")
        print("  {")
        for fi, frame in enumerate(frames):
            print(format_frame(frame, f"frame {fi} — {anim_comments[si][fi]}"))
        print("  },")
    print("};")
    print()

