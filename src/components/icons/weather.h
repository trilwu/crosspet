// Map WMO weather code to icon bitmap pointer
inline const uint8_t* getWeatherIcon(int wmoCode) {
    if (wmoCode <= 1)  return WeatherSunIcon;
    if (wmoCode == 2)  return WeatherPartlyCloudyIcon;
    if (wmoCode == 3)  return WeatherCloudyIcon;
    if (wmoCode <= 48) return WeatherFogIcon;
    if (wmoCode <= 57) return WeatherDrizzleIcon;
    if (wmoCode <= 82) return WeatherRainIcon;
    if (wmoCode <= 86) return WeatherSnowIcon;
    return WeatherThunderIcon;
}
