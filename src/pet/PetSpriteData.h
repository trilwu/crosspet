// ============================================================
// Sprite lookup — returns pointer to the 24-row array for the given frame
// frame: 0, 1, or 2 (animation frame index)
// ============================================================

inline const uint32_t* getSpriteRows(PetStage stage, uint8_t variant, uint8_t petType, uint8_t frame) {
  // Clamp frame to valid range
  if (frame > 2) frame = 0;

  // Dead sprite — shared, single design
  if (stage == PetStage::DEAD) return kSprite_Dead[frame];

  // Cat (type 1): variant support for Youngster/Companion
  if (petType == 1) {
    if (variant == 1) {
      if (stage == PetStage::YOUNGSTER) return kCat_Youngster_v1[frame];
      if (stage == PetStage::COMPANION) return kCat_Companion_v1[frame];
    } else if (variant == 2) {
      if (stage == PetStage::YOUNGSTER) return kCat_Youngster_v2[frame];
      if (stage == PetStage::COMPANION) return kCat_Companion_v2[frame];
    }
  }

  // Map stage to array index (EGG=0 .. ELDER=4)
  int idx;
  switch (stage) {
    case PetStage::EGG:       idx = 0; break;
    case PetStage::HATCHLING: idx = 1; break;
    case PetStage::YOUNGSTER: idx = 2; break;
    case PetStage::COMPANION: idx = 3; break;
    case PetStage::ELDER:     idx = 4; break;
    default:                  return kSprite_Dead[frame];
  }

  switch (petType) {
    case 0: return kChicken[idx][frame];
    case 1: return kCat[idx][frame];
    case 2: return kDog[idx][frame];
    case 3: return kDragon[idx][frame];
    case 4: return kBunny[idx][frame];
    case 5: return kRobot[idx][frame];
    case 6: return kBear[idx][frame];
    case 7: return kSlime[idx][frame];
    default: return kChicken[idx][frame];
  }
}

